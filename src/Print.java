
public class Print {
	
	
	
	// 객체의 매개변수 값 여러개 선언
	public static void println(int value) {
		System.out.println(value);
	}
	
	public static void println(double value) {
		System.out.println(value);
	}
	
	public static void println(boolean value) {
		System.out.println(value);
	}
	
	public static void println(String value) {
		System.out.println(value);
	}
	
}
